public class Arithmetic{
    public void Arithmetic(){
    }

    public static void main(String args[]){

        double A = 6;
        double B = 7;
        // addition
        System.out.println(A+B);
        // subtraction
        System.out.println(A-B);
        // multiplication
        System.out.println(A*B);
        // division
        System.out.println(A/B);
        System.out.println("============");
        System.out.println(B++);
        System.out.println(B%A);
        System.out.println(B--);
    }
}